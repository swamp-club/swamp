import type { DataVersionsEvent, EventHandlers } from "../../libswamp/mod.ts";
import type { Renderer } from "../renderer.ts";
import type { OutputMode } from "../output/output.ts";
import { UserError } from "../../domain/errors.ts";

class LogDataVersionsRenderer implements Renderer<DataVersionsEvent> {
  handlers(): EventHandlers<DataVersionsEvent> {
    return {
      resolving: () => {},
      completed: (e) => {
        // Log mode currently does JSON.stringify — preserve existing behavior
        console.log(JSON.stringify(e.data, null, 2));
      },
      error: (e) => {
        throw new UserError(e.error.message);
      },
    };
  }
}

class JsonDataVersionsRenderer implements Renderer<DataVersionsEvent> {
  handlers(): EventHandlers<DataVersionsEvent> {
    return {
      resolving: () => {},
      completed: (e) => {
        console.log(JSON.stringify(e.data, null, 2));
      },
      error: (e) => {
        throw new UserError(e.error.message);
      },
    };
  }
}

export function createDataVersionsRenderer(
  mode: OutputMode,
): Renderer<DataVersionsEvent> {
  switch (mode) {
    case "json":
      return new JsonDataVersionsRenderer();
    case "log":
      return new LogDataVersionsRenderer();
  }
}
